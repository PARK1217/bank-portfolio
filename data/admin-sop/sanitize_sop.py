def sanitize(text: str) -> str:
    out = text
    for old, new in EXACT_REPLACE:
        out = out.replace(old, new)
    # 빈 frontmatter 라인 정리
    out = re.sub(r">\s*\*\*\s*\*\*\s*\*\*\s*\*\*", "", out)
    out = re.sub(r"\n>\s*\n", "\n\n", out)
    # 연속 빈 줄 정리
    out = re.sub(r"\n{3,}", "\n\n", out)
    return out


def main() -> int:
    for mdfile in sorted(SYNTH.glob("*.md")):
        original = mdfile.read_text(encoding="utf-8")
        sanitized = sanitize(original)
        if sanitized != original:
            mdfile.write_text(sanitized, encoding="utf-8")
            print(f"  cleaned: {mdfile.name} ({len(original)} → {len(sanitized)})")
        else:
            print(f"  no change: {mdfile.name}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
